#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
		write(1, &i, 1);

}

int main(void)
{
	char t1[] = "ABCDEF";
	char t2[] = "ABCDEFi";

	ft_strcmp(t1, t2);
	return(0);
}
